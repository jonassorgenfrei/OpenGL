#define SHADED_STATE 0

#if SHADED_STATE == 1
	/*
	 * 0 - rustedIron
	 * 1 - gold
	 * 2 - grass
	 * 3 - plastic
	 * 4 - wall
	 */
	#define TEXTURE 0
#endif

#include <glad/glad.h>
#include <GLFW/glfw3.h>

#include <glm/glm.hpp>
