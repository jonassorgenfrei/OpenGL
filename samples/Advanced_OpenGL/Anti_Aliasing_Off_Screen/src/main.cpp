/* 
 *	Anti Aliasing - Off Screen
 *		-> fighting aliasing behavior to produce more smooth edges
 *		(super sample anti-aliasing [SSAA] -> res downsampled, when vis. output is updated in the framebuffer) extra resolution => major performance drawback
 *		(multisample anti-aliasing [MSAA] -> borrows concepts behind SSAA while implementing a much more efficient approach)
 *		
 *		Depth and Stencil Buffer are affected by multisampling too. (each increased by size of subsamples per pixel)
 *			Depth Testing: vertex's depth value is interpolated to each subsample before running the depth test
 *			Stancile Testing:	store stencil values per subsample, instead of per pixel 
 *
 *		Rendering to a multisampled framebuffer object hoes automatically.
 *		Whenever we draw anything while the framebuffer object is bound, the rasterizer will take care of all the multisample operations.
 */

